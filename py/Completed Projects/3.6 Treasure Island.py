left_right = input("LEFT or RIGHT?\n")
if left_right.lower() == "left":
  swim_wait = input("You reach a river and see a boat. WAIT or SWIM?\n")
  if swim_wait.lower() == "wait":
    which_door = input("You arrive at a haunted house. Which door do you choose? RED, BLUE or YELLOW?\n")
    if which_door.lower() == "yellow":
      print("You open the door and sitting in the middle of the room is a giant treasure chest. Congratulations, you win!")
    elif which_door.lower() == "blue":
      print("You are eaten by savage chihuahua beasts. Game over.")
    elif which_door.lower() == "red":
      print("You are burned by fire. Game over.")
    else:
      print("Better luck next time. Game over.")
  else:
    print("You are attacked by a vicious trout. Game over.")
else:
  print("You fell into a hole. Game Over.")